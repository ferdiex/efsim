from __future__ import annotations

import argparse
import json
import sys
from pathlib import Path

ROOT = Path(__file__).resolve().parents[1]
if str(ROOT) not in sys.path:
    sys.path.insert(0, str(ROOT))

import numpy as np


def parse_args():
    parser = argparse.ArgumentParser(description="Train a simple ANN from one or more imitation datasets.")
    parser.add_argument(
        "--dataset",
        type=str,
        nargs="+",
        default=["data/heuristic_dataset.npz"],
        help="One or more dataset paths generated by collect_heuristic_dataset.py",
    )
    parser.add_argument(
        "--output",
        type=str,
        default="models/ann_imitation.json",
        help="Path to exported ANN model JSON.",
    )
    parser.add_argument("--hidden-size", type=int, default=64, help="Hidden layer size.")
    parser.add_argument("--epochs", type=int, default=400, help="Training epochs.")
    parser.add_argument("--lr", type=float, default=0.005, help="Learning rate.")
    parser.add_argument("--seed", type=int, default=0, help="Random seed.")
    return parser.parse_args()


def relu(x: np.ndarray) -> np.ndarray:
    return np.maximum(0.0, x)


def relu_grad(x: np.ndarray) -> np.ndarray:
    return (x > 0.0).astype(np.float32)


def softmax(logits: np.ndarray) -> np.ndarray:
    shifted = logits - np.max(logits, axis=1, keepdims=True)
    exp = np.exp(shifted)
    return exp / np.sum(exp, axis=1, keepdims=True)


def one_hot(y: np.ndarray, num_classes: int) -> np.ndarray:
    out = np.zeros((len(y), num_classes), dtype=np.float32)
    out[np.arange(len(y)), y] = 1.0
    return out


def load_datasets(paths: list[str]) -> tuple[np.ndarray, np.ndarray]:
    xs = []
    ys = []

    for path in paths:
        dataset = np.load(path)
        obs = dataset["observations"].astype(np.float32)
        actions = dataset["actions"].astype(np.int64)

        xs.append(obs)
        ys.append(actions)

        print(f"loaded dataset: {path} samples={len(actions)} shape={obs.shape}")

    X = np.concatenate(xs, axis=0)
    y = np.concatenate(ys, axis=0)
    return X, y


def main():
    args = parse_args()
    rng = np.random.default_rng(args.seed)

    X, y = load_datasets(args.dataset)

    num_samples, input_dim = X.shape
    num_classes = int(np.max(y)) + 1

    X_mean = X.mean(axis=0, keepdims=True)
    X_std = X.std(axis=0, keepdims=True) + 1e-6
    Xn = (X - X_mean) / X_std

    y_onehot = one_hot(y, num_classes)

    class_counts = np.bincount(y, minlength=num_classes).astype(np.float32)
    class_weights = num_samples / (num_classes * np.maximum(class_counts, 1.0))
    sample_weights = class_weights[y].astype(np.float32)

    print(f"num_samples={num_samples}")
    print(f"input_dim={input_dim}")
    print(f"num_classes={num_classes}")
    print("class_counts:")
    for cls, count in enumerate(class_counts):
        print(f"  class={cls}: count={int(count)} weight={class_weights[cls]:.4f}")

    hidden_size = args.hidden_size

    W1 = rng.normal(0.0, 0.1, size=(input_dim, hidden_size)).astype(np.float32)
    b1 = np.zeros((hidden_size,), dtype=np.float32)
    W2 = rng.normal(0.0, 0.1, size=(hidden_size, num_classes)).astype(np.float32)
    b2 = np.zeros((num_classes,), dtype=np.float32)

    for epoch in range(args.epochs):
        z1 = Xn @ W1 + b1
        h1 = relu(z1)
        logits = h1 @ W2 + b2
        probs = softmax(logits)

        per_sample_loss = -np.sum(y_onehot * np.log(probs + 1e-8), axis=1)
        loss = np.sum(sample_weights * per_sample_loss) / np.sum(sample_weights)

        preds = np.argmax(probs, axis=1)
        acc = np.mean(preds == y)

        dlogits = (probs - y_onehot)
        dlogits *= sample_weights[:, None]
        dlogits /= np.sum(sample_weights)

        dW2 = h1.T @ dlogits
        db2 = np.sum(dlogits, axis=0)

        dh1 = dlogits @ W2.T
        dz1 = dh1 * relu_grad(z1)
        dW1 = Xn.T @ dz1
        db1 = np.sum(dz1, axis=0)

        W1 -= args.lr * dW1
        b1 -= args.lr * db1
        W2 -= args.lr * dW2
        b2 -= args.lr * db2

        if epoch % 20 == 0 or epoch == args.epochs - 1:
            pred_counts = np.bincount(preds, minlength=num_classes)
            print(
                f"epoch={epoch:03d} loss={loss:.4f} acc={acc:.4f} "
                f"pred_counts={pred_counts.tolist()}"
            )

    output_path = Path(args.output)
    output_path.parent.mkdir(parents=True, exist_ok=True)

    model = {
        "weights": [
            W1.tolist(),
            W2.tolist(),
        ],
        "biases": [
            b1.tolist(),
            b2.tolist(),
        ],
        "normalization": {
            "mean": X_mean.squeeze(0).tolist(),
            "std": X_std.squeeze(0).tolist(),
        },
        "history_length": 4,
        "base_obs_dim": 9,
    }

    with output_path.open("w", encoding="utf-8") as f:
        json.dump(model, f)

    print(f"saved model to: {output_path}")


if __name__ == "__main__":
    main()
